volatile uint8_t TimerCount;

void WaitTimer(uint8_t t)
{
    while (TimerCount < t) {
        tuh_task();
    }
    TimerCount = 0;
}

static int sx = 0, sy = 0;

int main()
{
    // stdio_init_all();
    InitKeys();
    Sound::Initialize();
    Video::Initialize();

    Sound::SetWave(0, wave1);
    Sound::SetWave(1, wave3);
    Sound::SetWave(2, wave2);

    {
        auto pSource = MonoPattern;
        auto pDestination = Video::TilePattern();
        for (auto i = 0; i < 0x40 * 4; ++i) {
            auto source = *pSource++;
            for (auto j = 0; j < 4; ++j) {
                uint8_t destination = 0;
                if ((source & 0x80) != 0) {
                    destination |= 0xf0;
                }
                if ((source & 0x40) != 0) {
                    destination |= 0x0f;
                }
                *pDestination++ = destination;
                source <<=2;
            }
        }
    }

    for (auto i = 0; i < 1000; ++i) {
        Video::TileMap()[i] = i % 0x40;
    }
    gpio_init(16);
    gpio_set_dir(16, GPIO_OUT);

    Sound::StartMelody(BGM_B, BGM_C);

    auto y = 0;
    while (true) {
        tuh_task();
        if (ScanKeys() != 0) {
            --sx;
            ++Video::TileMap()[0];
        }
        Video::ShowSprite(1, 4, y, 1);
        Video::ShowSprite(0, sx, sy, 0);
        // gpio_put(16, true);
        // sleep_ms(300);
        // gpio_put(16, false);
        // sleep_ms(300);
        ++y;
        // ++x;
        WaitTimer(60);
        Sound::StartMelody(hit);
    }
}
